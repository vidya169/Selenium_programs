package Logical_Program;


	public class example3_Multiply_2_Num_Without_Multi_Operator
	{
	public static void main(String[] args) 
	{
		
		int num1=50;
		int num2=4;
			
		int sum=0;     //  200
		
		//       	  5<=4   	
		for(int i=1; i<=num2; i++)
		{
			sum = sum+ num1;      // 150 + 50 =200
		}
		
		System.out.println(sum);  
	}
	}
	


